    private int mechReqWear(int wear) {
        if (wear == 0) {
            return 0;
        }
        float workforce = FullGame.data.findPlayerTeam().getWorkforce(4);
        return (int) ((wear * 200) / workforce);
    }

    private int mechReqDamage(int damage) {
        if (damage == 0) {
            return 0;
        }
        float workforce = FullGame.data.findPlayerTeam().getWorkforce(4);
        return (int) ((damage * 400) / workforce);
    }

    private int sparesReqWear(int wear) {
        if (wear == 0) {
            return 0;
        }
        if (wear < 8) {
            return 1;
        } else if (wear < 16) {
            return 2;
        } else if (wear < 24) {
            return 3;
        } else if (wear < 32) {
            return 4;
        } else {
            return 5;
        }
    }

    private int sparesReqDamage(int damage) {
        if (damage == 0) {
            return 0;
        }
        if (damage < 4) {
            return 1;
        } else if (damage < 8) {
            return 2;
        } else if (damage < 12) {
            return 3;
        } else if (damage < 16) {
            return 4;
        } else {
            return 5;
        }
    }

