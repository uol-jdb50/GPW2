    private void btnCar1RepairMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCar1RepairMouseClicked
        int repair = (FullGame.data.findPlayerTeam().car1mnt * 100) / car1mntReq;
        int spares = sparesReqWear((int) ((repair / 100.0f) * FullGame.data.findPlayerTeam().currentYearCars[0].chassis.wear));
        if (spares <= FullGame.data.findPlayerTeam().spares) {
            FullGame.data.findPlayerTeam().currentYearCars[0].chassis.wear -= (int) ((repair / 100.0f) * FullGame.data.findPlayerTeam().currentYearCars[0].chassis.wear);
            FullGame.data.findPlayerTeam().car1mnt = 0;
            FullGame.data.findPlayerTeam().spares -= spares;
        } else {
            showResultDialog();
        }
        updateScreenData();
    }//GEN-LAST:event_btnCar1RepairMouseClicked

